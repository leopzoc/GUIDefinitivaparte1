    private void AggiungOrarioFilmBottone(LocalTime Ora){
            JButton boton = new JButton("Ore: "+Ora);
         // Aggiungi il bottone alla lista
        orarioBoton.add(boton);
        OrarioFilm.add(boton);

            final int indice = orarioBoton.size() - 1; // Indice del bottone appena aggiunto
            boton.addActionListener(e -> {
                if (indiceOrario != -1) {
                    // Resetta il bordo del bottone precedentemente selezionato
                    orarioBoton.get(indiceOrario).setBorder(null);
                    // Opzionale: riabilita il bottone precedentemente selezionato
                    orarioBoton.get(indiceOrario).setEnabled(true);
                }
                // Imposta il bordo del bottone attualmente selezionato a rosso
                boton.setBorder(BorderFactory.createLineBorder(Color.RED, 2));
                indiceOrario = indice;

                // Disabilita tutti i bottoni tranne quello selezionato
                for (int i = 0; i < orarioBoton.size(); i++) {
                    if (i != indiceOrario) {
                        orarioBoton.get(i).setEnabled(false);
                    }
                }
            });

            // Altri settaggi del bottone...

    }

