                System.out.println("Percorso immagine: " + percorsoImmagine);
                System.out.println("Dimensioni jPanel1: " + jPanel1.getWidth() + "x" + jPanel1.getHeight());

                Image immagineRidimensionata = icona.getImage().getScaledInstance(jPanel1.getWidth(), jPanel1.getHeight(), Image.SCALE_SMOOTH);
